public class UpPoint {
	private int x, y;//defining variables as integer value
	
	UpPoint(int x, int y){this.x = x; this.y = y;}
	public String toString()//method that has a return value
	{
		return "x :"  + x + "y:" + y;//the return value of the method
		
	}

}
